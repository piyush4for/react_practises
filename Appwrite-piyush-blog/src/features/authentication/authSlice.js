import { createSlice } from "@reduxjs/toolkit";

const initialState = {
    status : false,
    userData: null
}

const authSlice = createSlice({
    name: "auth",
    initialState,
    reducers: {
        //state for updating state value in store
        //action for getting and pushing some values from frontend
        login: (state, action) => {
            state.status = true;
            state.userData = action.payload.userData;
        },
        logout: (state) => {
            state.status = false;
            state.userData = null;
        }
     }
})

//here actions means all reducers function 
export const {login, logout} = authSlice.actions;

export default authSlice.reducer;